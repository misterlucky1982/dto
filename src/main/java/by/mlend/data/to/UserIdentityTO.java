package by.mlend.data.to;

public class UserIdentityTO implements TransferObject {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	private String login;
	private String password;

	public String getLogin() {
		return login;
	}

	public void setLogin(String login) {
		this.login = login;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public static final int MAX_LOGIN_LENGTH = 24;
	public static final int MIN_LOGIN_LENGTH = 3;
	public static final int MAX_PASSWORD_LENGTH = 40;
	public static final int MIN_PASSWORD_LENGTH = 6;

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((login == null) ? 0 : login.hashCode());
		result = prime * result + ((password == null) ? 0 : password.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		UserIdentityTO other = (UserIdentityTO) obj;
		if (login == null) {
			if (other.login != null)
				return false;
		} else if (!login.equals(other.login))
			return false;
		if (password == null) {
			if (other.password != null)
				return false;
		} else if (!password.equals(other.password))
			return false;
		return true;
	}
	
	

}
